package javaprogram;
//Encapulation
public class EnStudent 
{
	private String name;
	private int age;

	// setter method 
	void setname(String n)
	{
		this.name=n;
	}
	void setage(int a)
	{
		this.age=a;
	}
	
	//getter method
	public String getName()
	{
		return name;
	}
	public int getAge()
	{
		return age;
	}
}
	

